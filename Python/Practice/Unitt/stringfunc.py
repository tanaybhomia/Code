t="this is a STRING"
print(t.capitalize()) 
	# Captializes the first letter of the string 
print(t.casefold())
	# lowers every letter in the string 
print(t.endswith("G"))
	# Returns true if string ends with G 
print(t.isalpha())
	# True if the string only consists of alphabets 
print(t.isdigit())
	# Prints true if the string only has digits or integers 
print(t.find("h"))
	# Returns the index value if it is present 
print(t.replace("a","A"))
print(t.count("s"))
