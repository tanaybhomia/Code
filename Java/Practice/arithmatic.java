class arith
{
    public static void main(String args[])
    {
        int a = 10;
        int b = 20;
        int ans;
        ans = a + b;
        System.out.println("Ans is "+ans);

        ans = a - b;
        System.out.println("Ans is "+ans);

        ans = a * b;
        System.out.println("Ans is "+ans);
    }
}